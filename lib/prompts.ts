function buildSystemPrompt(perspective: Perspective): string {
  if (perspective === "default") {
    return "";
  }

  const strategy = STRATEGIES[perspective];
  const label =
    perspective === "recruiter"
      ? "招聘方"
      : perspective === "business"
        ? "商务合作"
        : perspective === "reader"
          ? "同频的人"
          : "学术视角";

  return BASE_PROMPT.replace("{perspectiveLabel}", label).replace(
    "{strategy}",
    strategy,
  );
}

export function buildUserPrompt(
  perspective: Perspective,
  baseData: ResumeData,
): string {
  const systemPrompt = buildSystemPrompt(perspective);
  const baseDataStr = JSON.stringify(baseData, null, 2);

  const fullPrompt = systemPrompt.replace("{baseData}", baseDataStr);

  return fullPrompt;
}

export function buildMessages(
  perspective: Perspective,
  baseData: ResumeData,
): Array<{ role: "system" | "user"; content: string }> {
  return [
    {
      role: "system",
      content:
        "你是一个精准的 JSON 生成器。你的输出必须只包含合法的 JSON，不要有任何 markdown 标记、代码块或额外文字。",
    },
    {
      role: "user",
      content: buildUserPrompt(perspective, baseData),
    },
  ];
}
